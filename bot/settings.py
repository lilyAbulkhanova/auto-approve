# Назначение: конфигурация и тексты сообщений для бота.

from typing import List

# === Основные настройки ===

# Токен бота (выдаётся BotFather при создании бота).
# ⚠️ Обязательно вставь сюда свой токен перед запуском!
BOT_TOKEN: str = ""

# Список id пользователей, которым разрешено авто-одобрение постов.
# Все остальные будут получать сообщение "На модерации".
USER_ID_WHITELIST: List[int] = [111]

# Список id владельцев (админов), которые могут использовать команды:
# /start, /help, /id
OWNER_IDS: List[int] = [111]

# Минимальная цена поста в XTR.
# Если пост дешевле — бот отклоняет и отправляет предупреждение.
MIN_POST_PRICE: int = 5


# === Тексты сообщений ===

# Сообщение при команде /start (видно только владельцам).
START_MESSAGE: str = """
Привет!
Бот автоматически может принимать платные посты,
предложенные пользователями из WHITELIST

Команды бота (эти команды может вызвать только админ):
 - /start - выводит общую инфу
 - /id - выводит id
 - /help - выводит общую инфу
 
Бот проверяет от кого было отправлено сообщение,
если это не пользователь из WHITELIST, ему отправляется сообщение:
'Ваш пост проходит модерацию, пожалуйста ожидайте!"

Так же пост может иметь минимальную стоимость, если пользователь
выставляет цену < минимальной, ему отправляется сообщение: 
'Минимальная стоимость поста на данный момент составляет: N число звезд'
"""

# Сообщение при команде /help (сейчас = START_MESSAGE)
HELP_MESSAGE: str = START_MESSAGE

# Ответ пользователю, если его нет в whitelist → пост "на модерации".
MODERATION_MESSAGE: str = "Ваш пост проходит модерацию, пожалуйста ожидайте!"

# Ответ пользователю, если цена поста ниже минимальной.
# f-строка подставляет значение MIN_POST_PRICE.
MIN_PRICE_MESSAGE: str = f"Минимальная стоимость поста на данный момент составляет: {MIN_POST_PRICE} XTR"

# Ответ пользователю, если пост успешно принят ботом.
APPROVED_MESSAGE: str = "Ваш пост был автоматически одобрен ботом!"
